# '.' = empty, '#' = filled

def glyph_from_ascii(rows):
    """Convert list of 13 strings ('.'/'#') to list of int bytes."""
    result = []
    for row in rows:
        val = 0
        for i, ch in enumerate(row):
            if ch == '#':
                val |= (1 << (7 - i))
        result.append(f"0x{val:02X}")
    return result

